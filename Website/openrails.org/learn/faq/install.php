<h3>Which platforms does OR run on?</h3>
<p>
Open Rails is working well on Windows PCs from Windows XP* onwards. It does not work on Windows XBox, Windows Phone or non-Windows platforms.
</p><p>
* <small>Windows XP requires Service Pack 2 (SP2).</small>
</p>
<h3>32-bit or 64-bit Windows?</h3>
<p>OR is currently a 32-bit program that will run on either 32-bit Windows or 64-bit Windows. When run on a 64-bit Windows system there are some benefits as follows.
</p><p>
Programs compiled for 32-bit Windows are always limited to using a maximum of 2GB of memory. Many current PCs, especially 64-bit systems, have more than this. Fortunately it is possible to compile a program, for 64-bit systems only, which can use up to 4GB of memory and this is called a Large Address Aware (LAA) option. The downloads include both the usual version and the LAA version and you choose between them in the Options form.
</p>
<h3><span id="hardware_requirements">What hardware does OR require?</span></h3>
<ul>
<li>Processor : 2.0 GHz (32 or 64-bit)</li>
<li>Memory : minimum = 0.5 GB, recommended = 2 GB</li>
<li>Hard drive : 40 MB (but models require lots of space. The 6 routes in MSTS require 1.4 GB)</li>
<li>Graphics Hardware : minimum = 128 MB dedicated video RAM with Pixel Shader 2.0, recommended = 256 MB RAM with Pixel Shader 3.0</li>
<li>Graphics Software : as provided by Windows. OR currently uses DirectX<sup>&reg;</sup> - version 9.0c</li>
<li>Sound : compatible with DirectX<sup>&reg;</sup> - version 9.0c</li>
</ul>
<h3>Do I need Microsoft Train Simulator (MSTS) installed to use OR?</h3>
<p>
You do not need MSTS installed in order to use Open Rails. 
</p><p>
When MSTS is installed, then Open Rails can safely make use of all the MSTS content as it does not change any MSTS files.
</p><p>
Most non-MSTS routes, activities and rolling stock make use of some MSTS content, usually sound files, textures and cab interiors. If products designed for MSTS are used on a PC with OR but not MSTS, then they are likely to work but some sounds and textures will be missing. Such files will be listed with warnings in the OR log file.
</p>
<h3>How can I recognise add-on products for MSTS that require MSTS files?</h3>
<p>
Add-ons designed for MSTS may rely on files from the MSTS product. For instance:
<ul>
  <li>Routes provided with an Install.bat file that copies <i>shapes</i> and <i>textures</i> from the routes on the MSTS CDs.</li>
  <li>Locomotives that re-use <i>cabviews</i> or <i>sounds</i> from the locomotives on the MSTS CDs (see folder TRAINSET).</li>
  <li>Rolling stock that have sound management (SMS) files that refer to <i>wave</i> files from the MSTS CDs (see folder SOUND).</li>
</ul>
</p>
<h3>If I install OR, will it change my MSTS files?</h3>
<p>
No, OR does not modify any MSTS files.
</p>
<h3>Does OR report data back to its creators?</h3>
<p>
Not at the moment. Data on how OR is used will be very helpful for the project but, if we wish to collect usage data, we will inform you
beforehand and you will always be able to opt out.
</p>
