<h2>We Need Your Help</h2>
<p>
The Open Rails team, like all coding teams, cannot guarantee bullet-proof code. There are so many variables - so many routes and activities and locos - that our testing procedures cannot consider them all. We rely on our users to let us know when something unexpected happens.
</p>
<h2>Our Bug Tracker</h2>
<p>
We make good use of a neat website called <a href="http://launchpad.net/or" target="_blank">Launchpad</a> which tracks all our bugs for us, making sure that none are accidentally overlooked. 
As soon as you file a bug report there, the team members are notified and you can discuss the bug with them. The progress of a bug is
tracked and notified to you and in most cases a bug will progress through the stages <br><span class="tt">New > Confirmed > Triaged > Assigned > Fixed</span>
</p>
<h2>Suggestions</h2>
<p>
You can also use the Bug Tracker to send us an idea for our Wish List. Do this by submitting a bug report in the usual way and we will 
give it the category of "Wishlist".
</p>
<h2>Before Using The Bug Tracker</h2>
<p>
Before you rush off and enter a report, do consider:
</p>
<ul>
  <li>Is your problem really caused by a bug in Open Rails?</li>
  <li>or perhaps a fault in your model?</li>
  <li>or perhaps a feature that Open Rails has not provided yet?</li>
</ul>
<p>
If you are unsure, then use the forum <a href="http://www.elvastower.com/forums/index.php?/forum/244-maybe-its-a-bug/" target="_blank">Maybe It's A Bug</a> to ask for guidance. You may find that others have seen something similar or know how to correct your model.
</p>
<h2>Information About The Bug</h2>
<p>
Please attach to your report a copy of the file <span class="tt">OpenRailslog.txt</span> as this contains any error messages and lists your Open Rails settings,
the route, activity or consist. You can find this file on your Windows Desktop. If a screenshot would help, then attach that too.
</p>
