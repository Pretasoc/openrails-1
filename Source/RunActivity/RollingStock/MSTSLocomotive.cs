                                    data = 0;
                                    break;
                                }
                            case SignalHead.SIGASP.STOP_AND_PROCEED:
                                {
                                    data = 1;
                                    break;
                                }
                            case SignalHead.SIGASP.RESTRICTING:
                                {
                                    data = 2;
                                    break;
                                }
                            case SignalHead.SIGASP.APPROACH_1:
                                {
                                    data = 3;
                                    break;
                                }
                            case SignalHead.SIGASP.APPROACH_2:
                                {
