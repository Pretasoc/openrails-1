                _DestRectangle.Y = (int)(yratio * (_CabViewControl.PositionY + ypos));
                _DestRectangle.Width = (int)(xratio * _Gauge.Area.Width);
                _DestRectangle.Height = (int)(yratio * _Gauge.Area.Height);
            }
        }

