                                }
                            case SignalHead.SIGASP.CLEAR_1:
                            case SignalHead.SIGASP.CLEAR_2:
                            case SignalHead.SIGASP.CLEAR_3:
                            case SignalHead.SIGASP.CLEAR_4:
                                {
                                    data = 6;
                                    break;
                                }
                            default:
                                {
                                    data = 7;
                                    break;
                                }
                        }
                        break;
                    }
                default:
                    {
                        data = 0;
                        break;
                    }
            }
            return data;
